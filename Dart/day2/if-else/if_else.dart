/* 
If condition is TRUE → do something  
If condition is FALSE → do something else


syntax

if (condition) {
  // code
} else {
  // code
}

 */
// Example

void main() {
  int a = 10;
  if (a > 5) {
    print("A is big");
  } else {
    print("A is small");
  }
}



